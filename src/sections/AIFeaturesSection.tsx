export default function AIFeaturesSection() {
  const sectionRef = useRef<HTMLDivElement>(null)
  const cardsRef = useRef<(HTMLDivElement | null)[]>([])

  useEffect(() => {
    const ctx = gsap.context(() => {
      cardsRef.current.forEach((card, i) => {
        if (!card) return
        gsap.from(card, {
          opacity: 0,
          y: 50,
          scale: 0.95,
          duration: 0.7,
          ease: 'power3.out',
          scrollTrigger: {
            trigger: card,
            start: 'top 88%',
            toggleActions: 'play none none none',
          },
          delay: (i % 3) * 0.1,
        })
      })
    }, sectionRef)
    return () => ctx.revert()
  }, [])

  return (
    <section ref={sectionRef} className="relative py-20 sm:py-32 bg-[#0a0a0a]">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="text-center mb-12 sm:mb-16">
          <h2 className="text-3xl sm:text-4xl md:text-5xl font-bold text-white">
            Built for <span className="text-gradient">Real Businesses.</span>
          </h2>
          <p className="mt-4 text-lg text-[#888888] max-w-2xl mx-auto">
            Not just a chatbot. A complete AI phone agent trained to handle the specific needs of your industry.
          </p>
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-5 sm:gap-6">
          {features.map((feature, i) => (
            <div
              key={i}
              ref={(el) => { cardsRef.current[i] = el }}
              className="group glass rounded-2xl p-6 sm:p-7 hover:border-[#3b82f6]/40 hover:-translate-y-1 hover:shadow-xl hover:shadow-[#3b82f6]/5 transition-all duration-300 cursor-default"
            >
              <div className="w-11 h-11 rounded-lg bg-gradient-to-br from-[#3b82f6]/15 to-[#a855f7]/15 flex items-center justify-center text-[#3b82f6] group-hover:from-[#3b82f6]/25 group-hover:to-[#a855f7]/25 transition-all duration-300 mb-4">
                {feature.icon}
              </div>
              <h3 className="text-white font-semibold text-base sm:text-lg mb-2">{feature.title}</h3>
              <p className="text-[#888888] text-sm leading-relaxed">{feature.description}</p>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}
